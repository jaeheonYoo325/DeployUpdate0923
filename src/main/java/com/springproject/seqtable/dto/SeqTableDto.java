package com.springproject.seqtable.dto;

public class SeqTableDto {

	private int seqtableNo;
	private String chainId;
	private String seqNo;

	public int getSeqtableNo() {
		return seqtableNo;
	}

	public void setSeqtableNo(int seqtableNo) {
		this.seqtableNo = seqtableNo;
	}

	public String getChainId() {
		return chainId;
	}

	public void setChainId(String chainId) {
		this.chainId = chainId;
	}

	public String getSeqNo() {
		return seqNo;
	}

	public void setSeqNo(String seqNo) {
		this.seqNo = seqNo;
	}

}
